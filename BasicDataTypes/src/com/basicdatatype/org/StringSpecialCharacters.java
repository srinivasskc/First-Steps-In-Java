package com.basicdatatype.org;

public class StringSpecialCharacters {
	
	public static void main(String[] args) {
		String txt = "Exterior walls has 6\" Solid Cement blocks";
		System.out.println(txt);
		
		String txt1 = "Granide\'s platform with 2\' height";
		System.out.println(txt1);
		
		String txt2 = "Anchor\\S";
		System.out.println(txt2);
		
		
		
	}

}
