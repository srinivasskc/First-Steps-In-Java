//Package Name - Lower Case Letters

package com.learning.app;


//Public - Access Modifier: Outside of the class, Class - Keyword, App - Class Name.

public class App 
{ //Opening of the Class

	//Public - Access Modifier; 
	//Static is Java Keyword - Main Method is Static and We can call it directly.
	//No need to create an object of the Class to call this method
	// Main - By default method, called at starting directly from JVM.
	//Void - No return type. If return type, we can change to String/int
	//String[] args - command line arguments.
	
	public static void main(String[] args) 
	{
		System.out.println("Hello world Java Learning from Eclipse");
		
		System.out.print("This is my first Java Program...");
		System.out.print("Happy Learning!");
	}

} //Closing of the Class
