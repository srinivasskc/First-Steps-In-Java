package com.classes;

public class AreaOfCircle {
	
	public static void main(String[] args) {
		//Area of Circle formula: pi * r * r = pi r(square)
		
		double pi = 3.1416;
		double r = 10.8;
		
		double areaOfCircle = pi * r * r;
		
		System.out.println("Area of Circle: "+ areaOfCircle);
		
		
	}
	

}
