package com.classes;

public class UnicodeVariable {
	
	public static void main(String[] args) {
		int value = 'C';
		System.out.println("Value is : "+ value);		
		//Unicode Value of C is printed
		//http://awpacomputing.weebly.com/uploads/1/3/5/0/13509255/7059345.jpg?476
		
		char ch1 = 88; //Unicode value that represents X
		char ch2 = 'Y';
		System.out.println("ch1 and ch2: " + ch1 + " " + ch2);
		
		
		//Incrementing Characters
		char ch3 = 'A';
		System.out.println("Value of ch3 before increment:" + ch3);
		ch3++;
		System.out.println("Value of ch3 after increment: " +ch3);
		
		
		
	}

}
