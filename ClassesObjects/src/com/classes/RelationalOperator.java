package com.classes;

public class RelationalOperator {

	public static void main(String[] args) {
		
		System.out.println("10>9 is: " + (10>9));
		//true
		
		System.out.println("10<9 is: " + (10<9));
		//false
		
		System.out.println("10>=9 is: " + (10>=9));
		//true
		
		System.out.println("10<=9 is: " + (10<=9));
		//false
		
		System.out.println("10==9 is: " + (10==9));
		//false
	
		System.out.println("10!=9 is: " + (10!=9));
		//true
		
	}
	
}
